export class Song {
    title: string;
    artist: string;
    duration: string;
    genre: string;
    mood: string;
  
    constructor(title: string, artist: string, duration: string, genre: string, mood: string) {
      this.title = title;
      this.artist = artist;
      this.duration = duration;
      this.genre = genre;
      this.mood = mood;
    }
  }
  